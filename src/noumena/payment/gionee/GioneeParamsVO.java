package noumena.payment.gionee;

import noumena.payment.util.ChannelParamsVO;


public class GioneeParamsVO extends ChannelParamsVO
{
	private String callback_url;
	private String create_order_url;
	private String deliver_type;
	private String public_key;

	public String getCallback_url() {
		return callback_url;
	}
	public void setCallback_url(String callback_url) {
		this.callback_url = callback_url;
	}
	public String getCreate_order_url() {
		return create_order_url;
	}
	public void setCreate_order_url(String create_order_url) {
		this.create_order_url = create_order_url;
	}
	public String getDeliver_type() {
		return deliver_type;
	}
	public void setDeliver_type(String deliver_type) {
		this.deliver_type = deliver_type;
	}
	public String getPublic_key() {
		return public_key;
	}
	public void setPublic_key(String public_key) {
		this.public_key = public_key;
	}
}
