package noumena.payment.c4399;

import noumena.payment.util.ChannelParams;
import noumena.payment.util.ChannelParamsVO;

public class C4399Params extends ChannelParams
{
	public static String CHANNEL_ID		= "4399";
	
	public C4399ParamsVO getParams(String appid)
	{
		ChannelParamsVO vo = super.getParamsVO(appid);
		if (vo == null)
		{
			return new C4399ParamsVO();
		}
		else
		{
			return (C4399ParamsVO) vo;
		}
	}
	
}
