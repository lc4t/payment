package noumena.payment.duoku;

public class DuokuOrderVO
{
	String AppID;
	String OrderSerial;
	String CooperatorOrderSerial;
	String Sign;
	String Content;
	public String getAppID() {
		return AppID;
	}
	public void setAppID(String appID) {
		AppID = appID;
	}
	public String getOrderSerial() {
		return OrderSerial;
	}
	public void setOrderSerial(String orderSerial) {
		OrderSerial = orderSerial;
	}
	public String getCooperatorOrderSerial() {
		return CooperatorOrderSerial;
	}
	public void setCooperatorOrderSerial(String cooperatorOrderSerial) {
		CooperatorOrderSerial = cooperatorOrderSerial;
	}
	public String getSign() {
		return Sign;
	}
	public void setSign(String sign) {
		Sign = sign;
	}
	public String getContent() {
		return Content;
	}
	public void setContent(String content) {
		Content = content;
	}
	
	@Override
	public String toString() {
		String str = "AppID="+AppID+",OrderSerial="+OrderSerial+",CooperatorOrderSerial="+CooperatorOrderSerial+",Sign="+Sign+",Content="+Content;
		return str;
	}
	
}
