package noumena.payment.c7477sdk;

import noumena.payment.util.ChannelParams;
import noumena.payment.util.ChannelParamsVO;

public class C7477sdkParams extends ChannelParams
{
	public static String CHANNEL_ID		= "7477";
	
	public C7477SDKVO getParams(String appid)
	{
		ChannelParamsVO vo = super.getParamsVO(appid);
		if (vo == null)
		{
			return new C7477SDKVO();
		}
		else
		{
			return (C7477SDKVO) vo;
		}
	}
	
}
