package noumena.payment.youwan;

public class YouwanOrderVO
{
	private String status;
	private String trade_no;
	private String uid;
	private String app_id;
	private String app_trade_no;
	private String goods_name;
	private String goods_price;
	private String key;
	
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getTrade_no() {
		return trade_no;
	}
	public void setTrade_no(String tradeNo) {
		trade_no = tradeNo;
	}
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	public String getApp_id() {
		return app_id;
	}
	public void setApp_id(String appId) {
		app_id = appId;
	}
	public String getApp_trade_no() {
		return app_trade_no;
	}
	public void setApp_trade_no(String appTradeNo) {
		app_trade_no = appTradeNo;
	}
	public String getGoods_name() {
		return goods_name;
	}
	public void setGoods_name(String goodsName) {
		goods_name = goodsName;
	}
	public String getGoods_price() {
		return goods_price;
	}
	public void setGoods_price(String goodsPrice) {
		goods_price = goodsPrice;
	}
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
}
