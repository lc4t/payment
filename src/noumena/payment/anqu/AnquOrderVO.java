package noumena.payment.anqu;

public class AnquOrderVO
{
	private String cpappid;
	private String uid;
	private String money;
	private String cporder;
	private String order;
	private String sign;
	public String getCpappid() {
		return cpappid;
	}
	public void setCpappid(String cpappid) {
		this.cpappid = cpappid;
	}
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	public String getMoney() {
		return money;
	}
	public void setMoney(String money) {
		this.money = money;
	}
	public String getCporder() {
		return cporder;
	}
	public void setCporder(String cporder) {
		this.cporder = cporder;
	}
	public String getOrder() {
		return order;
	}
	public void setOrder(String order) {
		this.order = order;
	}
	public String getSign() {
		return sign;
	}
	public void setSign(String sign) {
		this.sign = sign;
	}
	
	
}
