package noumena.payment.onestore;

import java.util.List;

/**
 * oneStore 反馈的结果
 * 
 * @author kz
 * 
 */
public class OneStoreCbResultBean {

	private String status;
	private String detail;
	private String message;
	private String count;
	private List<OneStoreOrderVO> list;

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getDetail() {
		return detail;
	}

	public void setDetail(String detail) {
		this.detail = detail;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

	public String getCount() {
		return count;
	}

	public void setCount(String count) {
		this.count = count;
	}

	public List<OneStoreOrderVO> getList() {
		return list;
	}

	public void setList(List<OneStoreOrderVO> list) {
		this.list = list;
	}

	public OneStoreCbResultBean(String status, String detail, String message,
			String count, List<OneStoreOrderVO> list) {
		super();
		this.status = status;
		this.detail = detail;
		this.message = message;
		this.count = count;
		this.list = list;
	}

	public OneStoreCbResultBean() {
		super();
	}

}
