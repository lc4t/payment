package noumena.payment.adways;

public class AdwaysOrderVO
{
	private String identifier;
	private String achieve_id;
	private String point;
	private String user;
	private String confirm_flag;
	private String type;
	
	public String getIdentifier()
	{
		return identifier;
	}
	public void setIdentifier(String identifier)
	{
		this.identifier = identifier;
	}
	public String getAchieve_id()
	{
		return achieve_id;
	}
	public void setAchieve_id(String achieve_id)
	{
		this.achieve_id = achieve_id;
	}
	public String getPoint()
	{
		return point;
	}
	public void setPoint(String point)
	{
		this.point = point;
	}
	public String getUser()
	{
		return user;
	}
	public void setUser(String user)
	{
		this.user = user;
	}
	public String getConfirm_flag()
	{
		return confirm_flag;
	}
	public void setConfirm_flag(String confirm_flag)
	{
		this.confirm_flag = confirm_flag;
	}
	public String getType()
	{
		return type;
	}
	public void setType(String type)
	{
		this.type = type;
	}
}
