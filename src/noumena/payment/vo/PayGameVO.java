package noumena.payment.vo;

import java.util.List;

public class PayGameVO{
	private List<WebConvertVO> converts;
	private List<PayServerVO> servers;
	private String gameId;
	private String gameName;
	private String website;
	private String createTime;

	public String getWebsite()
	{
		return website;
	}

	public void setWebsite(String website)
	{
		this.website = website;
	}

	public List<WebConvertVO> getConverts() {
		return converts;
	}

	public void setConverts(List<WebConvertVO> converts) {
		this.converts = converts;
	}

	public List<PayServerVO> getServers() {
		return servers;
	}

	public void setServers(List<PayServerVO> servers) {
		this.servers = servers;
	}

	public String getGameId() {
		return gameId;
	}

	public void setGameId(String gameId) {
		this.gameId = gameId;
	}

	public String getGameName() {
		return gameName;
	}

	public void setGameName(String gameName) {
		this.gameName = gameName;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
}
