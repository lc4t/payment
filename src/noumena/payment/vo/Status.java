package noumena.payment.vo;

public class Status {
	private int status;
	private String uid;
	private String sid;

	public int getStatus() {
		return status;
	}

	public String getUid()
	{
		return uid;
	}

	public void setUid(String uid)
	{
		this.uid = uid;
	}

	public String getSid()
	{
		return sid;
	}

	public void setSid(String sid)
	{
		this.sid = sid;
	}

	public void setStatus(int status) {
		this.status = status;
	}
}
